package com.mpi.alienresearch.model;

public class Coordinates {
    float x;
    float y;

    
    public Coordinates() {
    }
    
    public float getX() {
        return x;
    }
    public void setX(float x) {
        this.x = x;
    }
    public float getY() {
        return y;
    }
    public void setY(float y) {
        this.y = y;
    }
}
